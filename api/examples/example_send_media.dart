import 'dart:typed_data';
import 'package:pocket_coach/peoplechain_core/peoplechain_core.dart';

// Sends a tiny generated PNG payload (~200 bytes) as encrypted media.
Future<void> main() async {
  final node = PeopleChainNode();
  await node.startNodeWithProgress(const NodeConfig(alias: 'MediaDemo'));
  await node.enableAutoMode();

  // 1x1 transparent PNG
  final bytes = Uint8List.fromList(const [
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
    0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4, 0x89,
    0x00, 0x00, 0x00, 0x0A, 0x49, 0x44, 0x41, 0x54,
    0x78, 0x9C, 0x63, 0x60, 0x00, 0x00, 0x00, 0x02,
    0x00, 0x01, 0xE5, 0x27, 0xD4, 0xA2, 0x00, 0x00,
    0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42,
    0x60, 0x82,
  ]);

  // Replace with discovered peer ed25519 key
  final peerEd25519 = 'REPLACE_ME_BASE64URL_ED25519';
  final res = await node.sendMedia(toPubKey: peerEd25519, bytes: bytes, mime: 'image/png');
  print('Media TX sent ok=${res.ok} id=${res.txId}');
}
